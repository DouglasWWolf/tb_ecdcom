/*
    This module synchronizes the CMAC "rx_aligned" signals to "clk"
*/


module ecdcom_link_state
(
    input       clk,
    input       rx0_aligned, rx1_aligned,
    output[1:0] link_state
);


//=============================================================================
// Synchronize "rx0_aligned" into "link_state[0]"
//=============================================================================
xpm_cdc_single #
(
    .DEST_SYNC_FF(4), .SRC_INPUT_REG(0) 
)
i_sync_0
(
    .src_clk    (),
    .src_in     (rx0_aligned),
    .dest_clk   (clk),
    .dest_out   (link_state[0])
);
//=============================================================================


//=============================================================================
// Synchronize "rx1_aligned" into "link_state[1]"
//=============================================================================
xpm_cdc_single #
(
    .DEST_SYNC_FF(4), .SRC_INPUT_REG(0) 
)
i_sync_1
(
    .src_clk    (),
    .src_in     (rx1_aligned),
    .dest_clk   (clk),
    .dest_out   (link_state[1])
);
//=============================================================================



endmodule
